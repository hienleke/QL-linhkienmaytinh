        // TODO add your handling code here:
        int kq = 0, sl = 0;
        try {
            sl = Integer.parseInt(txt_soluong.getText());
            this.soluong = sl;
        } catch (NumberFormatException e) {

            kq = 1;
        }

            // code block
        
        if (this.linhkien.getSoluong() < this.soluong) // ver Ke_hien
        {
            lb_thongbao.setText("Không đủ số lượng");

            return;
        }
        if (0 > this.soluong) {
            lb_thongbao.setText("Số lượng không được âm");

            return;
        }
        if (!(kq == 1 || sl == 0)) {
            this.dispose();
        } else if (sl == 0) {
            lb_thongbao.setText("Số lượng >=1");
            return;
        } else if (kq == 1) {
            lb_thongbao.setText("Chỉ chấp nhận kí tự số");
            return;
        }
            for (CT_hoadon ct : frame.getGiohang()) {
            if(ct.getLinhkien().getId().equals(frame.getjLabel103().getText().trim()))
            {
               int qq= ct.getSoluong();
              if(qq+sl > ct.getLinhkien().getSoluong())
              {
                  JOptionPane.showMessageDialog(this, "Không đủ số lượng");
                  return;
              }
                ct.setSoluong(sl+qq);
                frame.getModelgiohang().fireTableDataChanged();
                frame.getModelbanhang_linhkien().fireTableDataChanged();
                return ;
            }
        }
        this.hoadon = frame.getHd();
        hoadon.setNhanvien(frame.getNhanvien());
        giohang.add(new CT_hoadon(this.hoadon, this.linhkien, this.soluong));
        frame.getjTable11().setModel(frame.getModelgiohang());
