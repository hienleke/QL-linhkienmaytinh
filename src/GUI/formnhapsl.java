    private void txt_soluongKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_soluongKeyReleased
        // TODO add your handling code here:
        String key = txt_soluong.getText();
        if ((evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') || evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            txt_soluong.setEditable(true);
            lb_thongbao.setText("");
            try {
                int sl = Integer.parseInt(key);
                if (sl >= this.linhkien.getSoluong()) {
                    txt_soluong.setText("");
                    txt_soluong.setText(String.valueOf(this.linhkien.getSoluong()));
                }
            } catch (NumberFormatException e) {
            }

        } else {
            txt_soluong.setEditable(false);
            txt_soluong.setText("1");
            lb_thongbao.setText("Không nhập chữ");
        }
    }//GEN-LAST:event_txt_soluongKeyReleased

    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        // TODO add your handling code here:
        int sl = Integer.parseInt(txt_soluong.getText());
        if (sl < this.linhkien.getSoluong()) {
            sl++;
            txt_soluong.setText(String.valueOf(sl));
        }
    }//GEN-LAST:event_jLabel1MouseClicked

    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        // TODO add your handling code here:
        int sl = Integer.parseInt(txt_soluong.getText().toString());
        if (sl == 1) {
            txt_soluong.setText(String.valueOf(sl));
        } else {
            sl--;
            txt_soluong.setText(String.valueOf(sl));
        }
    }//GEN-LAST:event_jLabel3MouseClicked

